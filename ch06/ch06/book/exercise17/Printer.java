package ch06.book.exercise17;

public class Printer {

	
	static void println(String name) {
		System.out.println(name);
	}
	static void println(boolean name) {
		System.out.println(name);
	}
	static void println(int name) {
		System.out.println(name);
	}
	static void println(double name) {
		System.out.println(name);
	}
}
