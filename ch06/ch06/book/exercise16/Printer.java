package ch06.book.exercise16;

public class Printer {
	String name;
	
	void println(String name) {
		System.out.println(name);
	}
	
	void println(int name) {
		System.out.println(name);
	}
	
	void println(boolean name) {
		System.out.println(name);
	}
	
	void println(double name) {
		System.out.println(name);
	}
//	필드 생성자 메소드
	//스태틱이 붙으면 클래스에대한 멤버
	
}
