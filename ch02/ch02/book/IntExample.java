package ch02.book;

public class IntExample {

	public static void main(String[] args) {
		int var1 = 10;
		int var2 = 012;
		int var3 = 0xA;
		int var4 = 010;
		
		System.out.println(var1);
		System.out.println(var2);
		System.out.println(var3);
		System.out.println(var4);
		System.out.println(010);
		
		//0으로 시작하면 10진수 말고 다른걸로 인식하는듯?
	}

}
